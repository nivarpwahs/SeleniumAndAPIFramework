package api.models;

public class PostResponse {
    private String title;
    private String body;
    private int userId;
    private int id;

    // Getters and Setters

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }

    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }
}
